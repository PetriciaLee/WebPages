<h2>Sedam svjetskih čuda</h2>
<p>Sedam svjetskih čuda je sedam veličanstvenih čovjekovih dostignuća u arhitekturi i građevinarstvu. Kako civilizacija vremenom napreduje, tako postoji nekoliko takvih popisa.</p>
<p>Prvi popis svjetskih čuda sačinjen je navodno između 150. i 120. godine prije nove ere, a sačinio ga je grčki pjesnik Antipatros iz Sidona. Što ga je navelo da to učini, ne zna se. Ne zna se, štoviše, je li Antipatros poznavao građevine i skulpture na temelju vlastitog promatranja. Svih tih sedam znamenitosti bile su odabrane iz grčkog kuta gledanja, no samo jedno čudo našao je pjesnik na zemlji današnje Grčke: Fidijinog Zeusa. Tri čudesna djela bila su u Maloj Aziji: Kolos s Roda, Artemidin hram u Efesu i Mauzolej u Halikarnasu. Nešto dalji put bio je do piramida u Gizehu, a da čovjek ugleda viseće vrtove valjalo se potruditi sve do Babilona.</p>

<p>Čuda što su se, tako reći, nalazila ispred kućnog praga - Antipatros je izostavio: Akropolu u Ateni. Druge stvari nije mogao pribilježiti jer su već bile porušene, kao npr. Babilonski toranj. Pjesnik svojim suvremenicima nije nudio nikakve ruševine, već samo aktualne znamenitosti. To što je riječ upravo o sedam, a ne o trinaest ili dvadeset svjetskih čuda, ovisi o simboličnom značenju brojke sedam.</p>
<p>Kao "osmo čudo svijeta" smatrali su ljudi kasnijih godina nekoliko tuceta značajnih građevina razasutih po čitavu svijetu. Prve rekonstrukcije antičkih svjetskih čuda, punih fantazije, nastale su u vrijeme kad se Europa počela sjećati klasike. Marten de Vos (1532. - 1603.) i u njegovo vrijeme veoma slavni isusovac Athanasius Kircher, rođen 1601. u Fuldi, dali su prvi doprinos tome, a Fischer von Erlach (1656. - 1723.) približio se već nešto više stvarnosti. Vrijedna svjedočanstva naišla su tek u trenutku, kad su se arheolozi dali na posao.</p>

