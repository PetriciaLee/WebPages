Charles Spencer Chaplin (London, 16. travnja 1889. - Cousier-sur-Vevey, 25. prosinca 1977.), poznatiji kao Charlie Chaplin, engleski glumac, zvijezda nijemog filma, redatelj, scenarist i glazbenik.
