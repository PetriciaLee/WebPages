			<h3>
				Zanimljivosti otok Palagruža
			</h3>
			<p>
Crkveni izvori bilježe kako je na Čistu srijedu 9. ožujka 1177. godine na Palagruži boravio papa Aleksandar III. Putujući sa svojom pomorskom flotom od deset galija, bio je zadivljen ljepotom arhipelaga i poželio se zaustaviti. Papi je poslužena večera na maloj visoravni Palagruže Male, koja se od tada naziva Papina njiva.
			</p>
